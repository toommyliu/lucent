const parseCliOptions = (
  args: ReadonlyArray<string>,
  repoRoot: string,
): CliOptions => {
  let source = DEFAULT_SOURCE_FILE;
  let out = DEFAULT_OUTPUT_DIR;
  let includeSourceLinks = true;

  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index] ?? fail(`Unable to read argument at ${index}`);
    if (arg === "--") {
      continue;
    }
    if (arg === "--include-source-links") {
      includeSourceLinks = true;
      continue;
    }
    if (arg === "--no-source-links") {
      includeSourceLinks = false;
      continue;
    }
    if (arg === "--source") {
      source = args[index + 1] ?? fail("--source requires a path");
      index += 1;
      continue;
    }
    if (arg.startsWith("--source=")) {
      source = arg.slice("--source=".length);
      continue;
    }
    if (arg === "--out") {
      out = args[index + 1] ?? fail("--out requires a path");
      index += 1;
      continue;
    }
    if (arg.startsWith("--out=")) {
      out = arg.slice("--out=".length);
      continue;
    }
    fail(`Unknown argument: ${arg}`);
  }

  return {
    repoRoot,
    sourceFile: isAbsolute(source) ? source : resolve(repoRoot, source),
    outputDir: isAbsolute(out) ? out : resolve(repoRoot, out),
    includeSourceLinks,
  };
};

const normalizeGitRemoteUrl = (rawUrl: string): string | null => {
  const url = rawUrl.trim().replace(/\.git$/, "");
  if (url.startsWith("https://")) {
    return url;
  }
  if (url.startsWith("http://")) {
    return `https://${url.slice("http://".length)}`;
  }

  const sshMatch = url.match(/^git@([^:]+):(.+)$/);
  if (sshMatch) {
    return `https://${sshMatch[1]}/${sshMatch[2]}`;
  }

  return null;
};

const getGitSourceInfo = (
  repoRoot: string,
): Effect.Effect<GitSourceInfo | null> =>
  Effect.gen(function* () {
    const remoteUrl = yield* Effect.tryPromise(async () => {
      const result = await execFileAsync(
        "git",
        ["remote", "get-url", "origin"],
        { cwd: repoRoot },
      );
      return result.stdout;
    }).pipe(Effect.catch(() => Effect.succeed(null)));

    if (remoteUrl === null) {
      return null;
    }

    const repoUrl = normalizeGitRemoteUrl(remoteUrl);
    if (repoUrl === null) {
      return null;
    }

    const sourceRef = process.env[SOURCE_REF_ENV]?.trim();
    if (sourceRef) {
      return { repoUrl, ref: sourceRef };
    }

    return { repoUrl, ref: DEFAULT_SOURCE_REF };
  });

const buildSourceUrl = (
  git: GitSourceInfo | null,
  repoRoot: string,
  sourcePath: string,
  sourceLine: number,
): string | null => {
  if (git === null) {
    return null;
  }

  const repoRelativePath = relative(repoRoot, sourcePath).replaceAll("\\", "/");
  return `${git.repoUrl}/blob/${git.ref}/${repoRelativePath}#L${sourceLine}`;
};

const getSourceInfo = (
  options: CliOptions,
  git: GitSourceInfo | null,
  node: ts.Node,
): SourceInfo => {
  const sourceFile = node.getSourceFile();
  const sourceLine =
    sourceFile.getLineAndCharacterOfPosition(node.getStart(sourceFile)).line +
    1;

  return {
    sourcePath: relative(options.repoRoot, sourceFile.fileName).replaceAll(
      "\\",
      "/",
    ),
    sourceLine,
    sourceUrl: buildSourceUrl(
      git,
      options.repoRoot,
      sourceFile.fileName,
      sourceLine,
    ),
  };
};

const titleCase = (value: string): string =>
  value
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .split(/[\s_-]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

const kebabCase = (value: string): string =>
  value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[^A-Za-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .toLowerCase();

const anchorSlug = (value: string): string =>
  value
